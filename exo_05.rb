#EXPLIQUER #{} permet de faire comprendre a l'editeur ruby que nous voulans effectuer des operations

puts "On va compter le nombre d'heures de travail à THP" # utilisation de la fonction puts pour afficher une phrase

puts "Travail : #{10 * 5 * 11}" #Utilisation de la methode puts et de la fonction #{} pour afficher des chaines de caracteres et d'effectuer l'operation donner puis afficher le resultat.

puts "En minutes ça fait : #{10 * 5 * 11 * 60}" #Utilisation de la methode puts et la fonction #{} pour afficher des chaines de caracteres et d'effectuer l'operation donner puis afficher le resultat.

puts "Et en secondes ?" # Affichage d'une chaine de caractere.

puts 10 * 5 * 11 * 60 * 60 #Afficher le resultat d'un operation

puts "Est-ce que c'est vrai que 3 + 2 < 5 - 7 ?"# Affichage d'une chaine de caractere.

puts 3 + 2 < 5 - 7#Afficher la valeur de la comparaison sous forme boolean

puts "Ça fait combien 3 + 2 ? #{3 + 2}"#Utilisation de la methode puts et de la fonction #{} pour afficher des chaines de caracteres et d'effectuer l'operation donner puis afficher le resultat.

puts "Ça fait combien 5 - 7 ? #{5 - 7}"#Utilisation de la methode puts et de la fonction #{} pour afficher des chaines de caracteres et d'effectuer l'operation donner puis afficher le resultat.

puts "Ok, c'est faux alors !"# Affichage d'une chaine de caractere.

puts "C'est drôle ça, faisons-en plus :"# Affichage d'une chaine de caractere.

puts "Est-ce que 5 est plus grand que -2 ? #{5 > -2}"#Affichage d'une chaine de caratere et de la valeur de la comparaison sous forme boolean

puts "Est-ce que 5 est supérieur ou égal à -2 ? #{5 >= -2}"#Affichage d'une chaine de caratere et de la valeur de la comparaison sous forme boolean

puts "Est-ce que 5 est inférieur ou égal à -2 ? #{5 <= -2}"#Affichage d'une chaine de caratere et de la valeur de la comparaison sous forme boolean